const mongoose = require('mongoose');

// TODO: Create user schema -- No id. Is this correct?

const userSchema = new mongoose.Schema({
    firstName: {
        type: String,
        required: true,
    },
    lastName: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    },
    country: {
        type: String,
        required: true,
    },
    emailConfirmation: Boolean,
});

mongoose.model('user', userSchema);
